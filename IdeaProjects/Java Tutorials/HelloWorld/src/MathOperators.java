public class MathOperators {
    public static void main(String[] args){
        double x=10;
        int y=10;
        System.out.println("x + y = " + (x + y));
        System.out.println("x - y = " + (x - y));
        System.out.println("x / y = " + (x / y));
        System.out.println("x * y = " + (x * y));

        x++;
        y*=10;

        System.out.println("x = " + x);
        System.out.println("y = " + y);
        System.out.println(10%3);
        System.out.println(3*2.0);
    }
}
